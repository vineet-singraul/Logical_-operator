// 9. **Comparison with Logical Operators**  
//    **Question:** Is the number greater than 10 and less than 20?  

   #include <iostream>
   using namespace std;

   int main() {
       int number = 15;
       cout << (number > 10 && number < 20) << endl; // Output: 1 (true)
       return 0;
   }


